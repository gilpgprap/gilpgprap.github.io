const x = 5;
const y = 2;
console.log(x + y); // Suma -> 7
console.log(x - 2); // Resta -> 3
console.log(5 * y); // Multiplicación -> 10
//console.log(5 ** 2); // Potencia -> 25
console.log(5 / 2); // División -> 2.5
console.log(5 % 2); // Módulo o residuo -> 1
let z = 0;
console.log(++z); // Postincremento -> 1
// z === 1
console.log(z++); // Preincremento -> 1
// z === 2
console.log(z--); // Predecremento -> 2
// z === 1
console.log(--z); // Postdecremento -> 0
// z === 0